
// toUpperCase()    : Convierte todas las letras de la cadena a mayusculas.
// toLowerCase()    : Convierte todas las letras de la cadena a minusculas.
// charAt(n)        : Devuelve el caracter en la posción n.
// includes(cadena) : Verifica si la cadena contiene un texto específico y devuelve T-F.
// substring(inicio, fin)   : Devuelve una subcadena desde la posicion inicio hasta fina (sin incluir el fin).
// replace(viejo, nuevo)    : Reemplaza una parte de la cadena por otra.

let greeting = 'Hello, World';

console.log(greeting.toUpperCase());
console.log(greeting.toLocaleLowerCase());
console.log(greeting.charAt(5));
console.log(greeting.includes('llo'));
console.log(greeting.substring(3, 10));
console.log(greeting.replace('o', 0));




