// Un Set es una colección de valores únicos, lo que significa que no permite 
// duplicados. Los sets son útiles cuando necesitas garantizar que no haya valores
//  repetidos en tu conjunto de datos.


const numeros = new Set([1, 2, 3, 4, 5]);
console.log( numeros );

// Elimina los duplicados automaticamente
const numbers = new Set([1, 2, 2, 2, 3, 4, 3, 4, 5, 5, 5 ]);
console.log( numbers );

