// Métodos de Números


// a) .toFixed()
// Formatea el número con una cantidad fija de decimales.
let pi = 3.14159;
console.log(pi.toFixed(2)); // "3.14"


// b) parseInt() / parseFloat()
// Convierte texto a número entero o decimal.
let valor = "42.7";
console.log(parseInt(valor));  // 42
console.log(parseFloat(valor)); // 42.7
