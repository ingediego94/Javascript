// PROMPT
// Esta función muestra un cuadro de diálogo emergente (un pop-up) en la página donde el 
// usuario puede ingresar un valor. 
// El mensaje que se mostrará al usuario para que entienda qué tipo de dato debe ingresar.
// Un valor predeterminado (opcional), que se puede mostrar en el campo de entrada del cuadro de diálogo. 

prompt("Este es el encabezado del mensaje", "Especie de placeholder por defecto");
// Siempre será tratado como una cadena de texto (string), incluso si el usuario ingresa un número.



// ALERT 
// Muestra un cuadro de dialogo con un mensaje y un botón "Aceptar" pero no pide datos.

alert("This is an alert message of ALERT, no hace nada.");


// CONFIRM
// Pide una confirmación al usuario, devolviendo un valor verdadero si el usuario selecciona "Aceptar" 
// y falso si selecciona "Cancelar".

confirm("Are you a coder?");

