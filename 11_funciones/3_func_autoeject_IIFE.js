/*
FUNCIONES AUTOEJECUTABLES (IIFE - Immediately 
Invoked Function Expression)

Una IIFE es una función que se ejecuta inmediatamente 
después de ser definida. Esto es útil cuando queremos ejecutar 
una función de manera aislada, sin contaminar el espacio de nombres global (Los guardianes).
*/

(function(){
    console.log('This is an IIFE');
})();